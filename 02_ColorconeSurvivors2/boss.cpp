//=========================================================================================
//ボスの初期化処理
//=========================================================================================
HRESULT CBoss::Init(void)
{
	//オブジェクトの初期化処理
	CObjectX::Init("data\\MODEL\\boss_00.x");

	//種類の設定
	SetType(TYPE_BOSS);

	return S_OK;
}

//=========================================================================================
//ボスの終了処理
//=========================================================================================
void CBoss::Uninit(void)
{
	//オブジェクトの終了処理
	CObjectX::Uninit();
}

//=========================================================================================
//ボスの更新処理
//=========================================================================================
void CBoss::Update(void)
{
	//デバッグ情報取得
	CDebugProc* pDebug = CManager::GetManager()->GetDebugProc();

	//位置情報取得処理
	D3DXVECTOR3 pos = CObjectX::GetPosition();

	//ボスの座標取得
	D3DXVECTOR3 BossPos = CObjectX::GetPosition();
	BossPos = D3DXVECTOR3(0.0f,-200.0f,300.0f);

	//位置更新
	SetPosition(BossPos);

	//デバッグ表示
	pDebug->Print("[エネミー] ( X:%f Y:%f Z:%f )\n\n", pos.x, pos.y, pos.z);
}

//=========================================================================================
//ボスの描画処理
//=========================================================================================
void CBoss::Draw(void)
{
	//オブジェクトの描画処理
	CObjectX::Draw();
}

//=========================================================================================
//ボスの生成処理
//=========================================================================================
CBoss* CBoss::Create(void)
{
	//ポインタの変数を宣言
	CBoss* pBoss;

	//オブジェクト3Dの生成
	pBoss = new CBoss;

	if (pBoss != NULL)
	{//使用されているとき

		//初期化処理
		pBoss->Init();
	}

	//オブジェクト情報を返す
	return pBoss;
}

//=========================================================================================
//ボスのヒット処理
//=========================================================================================
void CBoss::Hit(void)
{
	//スコアの情報取得
	CScore* pScore = CGame::GetScore();

	//スコアの加算
	pScore->AddScore(100);
}