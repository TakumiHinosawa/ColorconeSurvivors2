//=========================================================================================
//ゲームの初期化処理	
//=========================================================================================
HRESULT CGame::Init(void)
{
	//サウンド情報取得
	CSound *pSound = CManager::GetManager()->GetSound();

	//********************************************************
	//カメラの生成
	//********************************************************
	if (m_pCamera == nullptr)
	{//使用されていないとき

		//生成
		m_pCamera = new CCamera;
	}
	if (m_pCamera != nullptr)
	{//使用されているとき

		//カメラの初期化処理
		m_pCamera->Init();
	}

	//********************************************************
	//ライトの生成
	//********************************************************
	if (m_pLight == nullptr)
	{//使用されていないとき

		//生成
		m_pLight = new CLight;
	}

	if (m_pLight != nullptr)
	{//使用されているとき

		//ライトの初期化処理
		m_pLight->Init();
	}

	//********************************************************
	//テクスチャ読み込み
	//********************************************************
	CHitpoint::Load();
	CNumber::Load();
	CBlowngage::Load();
	CBullet3D::Load();
	CPoint::Load();
	CExplosion3D::Load();
	CEffect::Load();
	CHpbg::Load();
	CPercent::Load();

	//********************************************************
	//プレイヤー
	//********************************************************
	if (m_pPlayerX == nullptr)
	{//使用されていないとき

		//プレイヤー作成
		m_pPlayerX = CPlayerX::Create();
	}

	//********************************************************
	//スカイボックス
	//********************************************************
	if (m_pSkybox == nullptr)
	{//使用されていないとき

		//プレイヤー作成
		m_pSkybox = CSkybox::Create();

		m_pSkybox->SetPosition(D3DXVECTOR3(0.0f,-320.0f,0.0f));
	}

	//********************************************************
	//ボス
	//********************************************************
	if (m_pBoss == nullptr)
	{//使用されていないとき

		//ボス生成
		m_pBoss = CBoss::Create();
	}

	//********************************************************
	//建物 配置
	//********************************************************
	struct SBuildingInfo
	{
		CBuilding::TYPE mType;
		float mX;
		float mY;
		float mZ;
	};

	SBuildingInfo buildingInfo[] =
	{
		// チュートリアル 
		{ CBuilding::TYPE_START, 0.0f,-200.0f,0.0f },
	};

	for (int nCnt = 0; nCnt < NUM_BUILD; nCnt++)
	{
		if (m_apBuilding[nCnt] == NULL)
		{//使用されていないとき

			//プレイヤー作成
			m_apBuilding[nCnt] = CBuilding::Create();
			m_apBuilding[nCnt]->SetType(buildingInfo[nCnt].mType);
			m_apBuilding[nCnt]->SetPosition(D3DXVECTOR3(buildingInfo[nCnt].mX, buildingInfo[nCnt].mY, buildingInfo[nCnt].mZ));
		}
	}

	//********************************************************
	//ヒットポイント
	//********************************************************
	if (m_pHpbg == nullptr)
	{//使用されていないとき

		//スコアオブジェクトの情報を取得
		m_pHpbg = CHpbg::Create();
	}

	//********************************************************
	//ヒットポイント
	//********************************************************
	if (m_pHitpoint == nullptr)
	{//使用されていないとき

		//スコアオブジェクトの情報を取得
		m_pHitpoint = CHitpoint::Create();
	}

	//********************************************************
	//スコア
	//********************************************************
	if (m_pScore == nullptr)
	{//使用されていないとき

		//スコアオブジェクトの情報を取得
		m_pScore = CScore::Create();

		//オブジェクト設定
		m_pScore->SetScore();
	}

	//********************************************************
	//吹っ飛び
	//********************************************************
	if (m_pBlown == nullptr)
	{//使用されていないとき

		//スコアオブジェクトの情報を取得
		m_pBlown = CBlown::Create();

		//オブジェクト設定
		m_pBlown->Set();
	}

	//********************************************************
	//パーセント
	//********************************************************
	if (m_pPercent == nullptr)
	{//使用されていないとき

		//スコアオブジェクトの情報を取得
		m_pPercent = CPercent::Create();
	}

	//********************************************************
	//サウンド
	//********************************************************
	pSound->PlaySound(CSound::SOUND_LABEL_BGM_GAME);

	return S_OK;
}

//=============================================================================
//ゲームの終了処理
//=============================================================================
void CGame::Uninit(void)
{
	//オブジェクトの破棄
	CObject::ReleaseAll();

	//********************************************************
	//スコア
	//********************************************************
	if (m_pScore != nullptr)
	{//使用されていたら

		//スコアの終了処理
		m_pScore->Uninit();

		//破棄
		delete m_pScore;

		//初期化
		m_pScore = nullptr;
	}

	//********************************************************
	//吹っ飛び
	//********************************************************
	if (m_pBlown != nullptr)
	{//使用されていたら

		//吹っ飛びの終了処理
		m_pBlown->Uninit();

		//破棄
		delete m_pBlown;

		//初期化
		m_pBlown = nullptr;
	}

	//********************************************************
	//ライト
	//********************************************************
	if (m_pLight != nullptr)
	{
		//終了処理
		m_pLight->Uninit();

		//破棄
		delete m_pLight;

		//初期化
		m_pLight = nullptr;
	}

	//********************************************************
	//カメラ
	//********************************************************	
	if (m_pCamera != nullptr)
	{
		//終了処理
		m_pCamera->Uninit();

		//破棄
		delete m_pCamera;

		//初期化
		m_pCamera = nullptr;
	}

	//********************************************************
	//プレイヤー
	//********************************************************
	if (m_pPlayerX != nullptr)
	{//使用されているとき

		//初期化
		m_pPlayerX = nullptr;
	}

	//********************************************************
	//スカイボックス
	//********************************************************
	if (m_pSkybox != nullptr)
	{//使用されていないとき

		//プレイヤー作成
		m_pSkybox = nullptr;
	}

	//********************************************************
	//ボス
	//********************************************************
	if (m_pBoss != nullptr)
	{//使用されているとき

		//初期化
		m_pBoss = nullptr;
	}

	//********************************************************
	//ヒットポイント
	//********************************************************
	if (m_pHitpoint != nullptr)
	{//使用されているとき

		//初期化
		m_pHitpoint = nullptr;
	}

	//********************************************************
	//パーセント
	//********************************************************
	if (m_pPercent != nullptr)
	{//使用されていたら

		//初期化
		m_pPercent = nullptr;
	}

	//********************************************************
	//ヒットポイントBG
	//********************************************************
	if (m_pHpbg != nullptr)
	{//使用されているとき

		//初期化
		m_pHpbg = nullptr;
	}

	//********************************************************
	//建物
	//********************************************************
	for (int nCnt = 0; nCnt < NUM_BUILD; nCnt++)
	{
		if (m_apBuilding != nullptr)
		{//使用されているとき

			//初期化
			m_apBuilding[nCnt] = nullptr;
		}
	}

	//各種テクスチャ破棄
	CHitpoint::Unload();
	CNumber::Unload();
	CBullet3D::Unload();
	CExplosion3D::Unload();
	CEffect::Unload();
	CPoint::Unload();
	CHpbg::Unload();
	CBlowngage::Unload();
	CPercent::Unload();
}

//=============================================================================
//ゲームの更新処理
//=============================================================================
void CGame::Update(void)
{
	//********************************************************
	//スコア
	//********************************************************
	if (m_pScore != nullptr)
	{//使用されていたら

		//更新処理
		m_pScore->Update();
	}

	//********************************************************
	//吹っ飛び
	//********************************************************
	if (m_pBlown != nullptr)
	{//使用されていたら

		//更新処理
		m_pBlown->Update();
	}

	//********************************************************
	//カメラ
	//********************************************************
	if (m_pCamera != nullptr)
	{//使用されていたら

		//更新処理
		m_pCamera->Update();
	}

	//プレイヤーの位置情報取得
	D3DXVECTOR3 pos = m_pPlayerX->GetPosition();
	
	if (pos.y <= -700.0f)
	{// プレイヤーが落ちた場合

		//サウンド情報取得
		CSound *pSound = CManager::GetManager()->GetSound();

		if (m_pScore != nullptr)
		{//使用されていたら

			//スコアの保存
			m_pScore->SaveScore();
		}

		//画面遷移
		CFade::GetInstance()->SetFade(CScene::MODE_RANKING);
	}
}

//=============================================================================
//ゲームの描画処理
//=============================================================================
void CGame::Draw(void)
{
	//カメラ情報取得
	CCamera *pCamera = CGame::GetCamera();

	//カメラの描画
	pCamera->SetCamera();
}

