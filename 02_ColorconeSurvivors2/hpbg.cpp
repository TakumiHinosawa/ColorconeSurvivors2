//*****************************************************************************************
//静的メンバ変数初期化
//*****************************************************************************************
LPDIRECT3DTEXTURE9 CHpbg::m_pTexture = nullptr;

//=========================================================================================
//ヒットポイントのコンストラクタ
//=========================================================================================
CHpbg::CHpbg()
{

}

//=========================================================================================
//ヒットポイントのデストラクタ
//=========================================================================================
CHpbg::~CHpbg()
{

}

//=========================================================================================
//ヒットポイントのテクスチャ読み込み
//=========================================================================================
HRESULT CHpbg::Load(void)
{
	//オブジェクト取得
	CRenderer* pRenderer = CManager::GetManager()->GetRenderer();

	//デバイス取得
	LPDIRECT3DDEVICE9 pDevice = pRenderer->GetDevice();

	//テクスチャの読み込み
	D3DXCreateTextureFromFile(pDevice,
		"data\\TEXTURE\\bar.png",
		&m_pTexture);

	return S_OK;
}

//=========================================================================================
//ヒットポイントのテクスチャ破棄
//=========================================================================================
void CHpbg::Unload(void)
{
	//テクスチャの破棄
	if (m_pTexture != NULL)
	{
		m_pTexture->Release();
		m_pTexture = NULL;
	}
}

//=========================================================================================
//ヒットポイントの初期化処理
//=========================================================================================
HRESULT CHpbg::Init(void)
{
	//オブジェクトの初期化処理
	CObject2D::Init();

	//種類の設定
	SetType(TYPE_HPBG);

	return S_OK;
}

//=========================================================================================
//ヒットポイントの終了処理
//=========================================================================================
void CHpbg::Uninit(void)
{
	//オブジェクトの終了処理
	CObject2D::Uninit();
}

//=========================================================================================
//ヒットポイントの更新処理
//=========================================================================================
void CHpbg::Update(void)
{
	//位置設定
	Set(D3DXVECTOR3(SCREEN_WIDTH / 2 - 10.0f, 40.0f, 0.0f), D3DXVECTOR3(0.0f, 0.0f, 0.0f), 30.0f, 300.0f);
}

//=========================================================================================
//ヒットポイントの描画処理
//=========================================================================================
void CHpbg::Draw(void)
{
	//オブジェクトの描画処理
	CObject2D::Draw();
}

//=========================================================================================
//ヒットポイントの設定処理
//=========================================================================================
CHpbg* CHpbg::Create(void)
{
	//ポインタの変数を宣言
	CHpbg* pHp;

	//オブジェクト2Dの生成
	pHp = new CHpbg;

	if (pHp != nullptr)
	{//使用されているとき

		//初期化処理
		pHp->Init();

		//テクスチャの割り当て
		pHp->BindTexture(m_pTexture);
	}

	//オブジェクト情報を返す
	return pHp;
}
