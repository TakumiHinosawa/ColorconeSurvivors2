#include "percent.h"
#include "renderer.h"
#include "manager.h"
#include "object2D.h"

//*****************************************************************************************
//静的メンバ変数初期化
//*****************************************************************************************
LPDIRECT3DTEXTURE9 CPercent::m_pTexture = nullptr;

//=========================================================================================
//パーセントのコンストラクタ
//=========================================================================================
CPercent::CPercent()
{

}

//=========================================================================================
//パーセントのデストラクタ
//=========================================================================================
CPercent::~CPercent()
{

}

//=========================================================================================
//パーセントのテクスチャ読み込み
//=========================================================================================
HRESULT CPercent::Load(void)
{
	//オブジェクト取得
	CRenderer* pRenderer = CManager::GetManager()->GetRenderer();

	//デバイス取得
	LPDIRECT3DDEVICE9 pDevice = pRenderer->GetDevice();

	//テクスチャの読み込み
	D3DXCreateTextureFromFile(pDevice,
		"data\\TEXTURE\\percent.png",
		&m_pTexture);

	return S_OK;
}

//=========================================================================================
//パーセントのテクスチャ破棄
//=========================================================================================
void CPercent::Unload(void)
{
	//テクスチャの破棄
	if (m_pTexture != nullptr)
	{
		m_pTexture->Release();
		m_pTexture = nullptr;
	}
}

//=========================================================================================
//パーセントの初期化処理
//=========================================================================================
HRESULT CPercent::Init(void)
{
	//オブジェクトの初期化処理
	CObject2D::Init();

	//種類の設定
	SetType(TYPE_PERCENT);

	return S_OK;
}

//=========================================================================================
//パーセントの終了処理
//=========================================================================================
void CPercent::Uninit(void)
{
	//オブジェクトの終了処理
	CObject2D::Uninit();
}

//=========================================================================================
//パーセントの更新処理
//=========================================================================================
void CPercent::Update(void)
{
	//位置設定
	Set(D3DXVECTOR3(SCREEN_WIDTH - 140.0f,SCREEN_HEIGHT - 25.0f,0.0f), D3DXVECTOR3(0.0f, 0.0f, 0.0f), 30.0f, 30.0f);
}

//=========================================================================================
//パーセントの描画処理
//=========================================================================================
void CPercent::Draw(void)
{
	//オブジェクトの描画処理
	CObject2D::Draw();
}

//=========================================================================================
//パーセントの設定処理
//=========================================================================================
CPercent* CPercent::Create(void)
{
	//ポインタの変数を宣言
	CPercent* pPercent;

	//オブジェクト2Dの生成
	pPercent = new CPercent;

	if (pPercent != nullptr)
	{//使用されているとき

		//初期化処理
		pPercent->Init();

		//テクスチャの割り当て
		pPercent->BindTexture(m_pTexture);
	}

	//オブジェクト情報を返す
	return pPercent;
}
