CEnemyX* CSpawner::m_pEnemy = nullptr;

//=========================================================================================
//マクロ定義
//=========================================================================================
#define ENEMYMOVE	(2.0f)		//移動量

//*****************************************************************************************
//静的メンバ変数
//*****************************************************************************************
int CSpawner::m_Count = 0;

