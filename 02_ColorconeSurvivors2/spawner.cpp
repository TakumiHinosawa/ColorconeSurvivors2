//=========================================================================================
//スポナーの生成処理
//=========================================================================================
CSpawner* CSpawner::Create(void)
{
	//ポインタの変数を宣言
	CSpawner* pSpawner;

	//オブジェクト3Dの生成
	pSpawner = new CSpawner;

	if (pSpawner != nullptr)
	{//使用されているとき

		//初期化処理
		pSpawner->Init();
	}

	//オブジェクト情報を返す
	return pSpawner;
}

//=========================================================================================
//スポナーのヒット処理
//=========================================================================================
void CSpawner::Hit(void)
{
	//スコアの情報取得
	CScore* pScore = CGame::GetScore();

	//スコアの加算
	pScore->AddScore(100);

	//終了処理
	CSpawner::Uninit();
}

