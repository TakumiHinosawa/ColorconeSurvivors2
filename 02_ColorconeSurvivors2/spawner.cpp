//*****************************************************************************************
//静的メンバ変数
//*****************************************************************************************
CEnemyX* CSpawner::m_pEnemy = nullptr;

//=========================================================================================
//マクロ定義
//=========================================================================================
#define ROTATION	(1.2f)		//移動量

//*****************************************************************************************
//静的メンバ変数
//*****************************************************************************************
int CSpawner::m_Count = 0;

//=========================================================================================
//スポナーのコンストラクタ	
//=========================================================================================
CSpawner::CSpawner()
{

}

//=========================================================================================
//スポナーのデストラクタ
//=========================================================================================
CSpawner::~CSpawner()
{

}

//=========================================================================================
//スポナーの初期化処理
//=========================================================================================
HRESULT CSpawner::Init(void)
{
	//オブジェクトの初期化処理
	CObjectX::Init("data\\MODEL\\spawner_00.x");

	//種類の設定
	SetType(TYPE_SPAWNER);

	//位置更新
	SetPosition(D3DXVECTOR3(0.0f, -200.0f, -200.0f));

	//********************************************************
	//エネミー
	//********************************************************
	if (m_pEnemy == nullptr)
	{//使用されていないとき

		//プレイヤー作成
		m_pEnemy = CEnemyX::Create();
	}

	return S_OK;
}

//=========================================================================================
//スポナーの終了処理
//=========================================================================================
void CSpawner::Uninit(void)
{
	//オブジェクトの終了処理
	CObjectX::Uninit();

	//********************************************************
	//エネミー
	//********************************************************
	if (m_pEnemy != nullptr)
	{//使用されているとき

		//初期化
		m_pEnemy = nullptr;
	}
}

//=========================================================================================
//スポナーの更新処理
//=========================================================================================
void CSpawner::Update(void)
{
	//デバッグ情報取得
	CDebugProc* pDebug = CManager::GetManager()->GetDebugProc();

	//エネミー角度取得
	D3DXVECTOR3 EnemyRot = CObjectX::GetRot();

	// 毎フレーム、回転角度を増加
	EnemyRot.y += ROTATION * 0.016666f;

	//向き更新
	SetRot(EnemyRot);

	//デバッグ表示
	pDebug->Print("[スポナー] ( X:%f Y:%f Z:%f )\n\n", EnemyRot.x, EnemyRot.y, EnemyRot.z);
}

//=========================================================================================
//スポナーの描画処理
//=========================================================================================
void CSpawner::Draw(void)
{
	//オブジェクトの描画処理
	CObjectX::Draw();
}

//=========================================================================================
//スポナーの生成処理
//=========================================================================================
CSpawner* CSpawner::Create(void)
{
	//ポインタの変数を宣言
	CSpawner* pSpawner;

	//オブジェクト3Dの生成
	pSpawner = new CSpawner;

	if (pSpawner != nullptr)
	{//使用されているとき

		//初期化処理
		pSpawner->Init();
	}

	//オブジェクト情報を返す
	return pSpawner;
}

//=========================================================================================
//スポナーのヒット処理
//=========================================================================================
void CSpawner::Hit(void)
{
	//スコアの情報取得
	CScore* pScore = CGame::GetScore();

	//スコアの加算
	pScore->AddScore(100);

	//終了処理
	CSpawner::Uninit();
}

//=========================================================================================
//スポナーのキル数
//=========================================================================================
int CSpawner::GetKillCount(void)
{
	return m_Count;
}

//=========================================================================================
//スポナーのキル数
//=========================================================================================
void CSpawner::SetKillCount(int nNum)
{
	m_Count = nNum;
}